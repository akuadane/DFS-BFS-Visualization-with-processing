def solve_puzzle_bfs(puzzle,goal,init):
    n = len(puzzle)
    
    visited = [[0]*n for _ in range(n)]
    visited[init[0]][init[1]]=init
    directions = [(0,1),(1,0),(0,-1),(-1,0)]
    explored=[init]
    
    q = []
    q.append(init)
    
    while q :
        temp_cur = q.pop(0)
        
        for r,c in directions:
            nxt_r = temp_cur[0] + r
            nxt_c = temp_cur[1] + c

            if min(nxt_r,nxt_c)<0 or max(nxt_r,nxt_c)>=n or visited[nxt_r][nxt_c]!=0 or puzzle[nxt_r][nxt_c]==0:
                continue
            visited[nxt_r][nxt_c]=temp_cur
            explored.append((nxt_r,nxt_c))
            if (nxt_r,nxt_c) == goal:
                return recreate_path(visited,goal,init),explored
            
            q.append((nxt_r,nxt_c))
    
    return [],explored

def recreate_path(visited,goal,init):

    path = [goal]
    parent = visited[goal[0]][goal[1]]
    
    while parent!=0 and parent!=init:
        path.append(parent)
        
        parent = visited[parent[0]][parent[1]]
    path.append(init)
    path.reverse()
    return path


